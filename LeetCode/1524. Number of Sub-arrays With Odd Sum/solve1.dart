//  1       3                    5
// {1}  {1} {3}        {1} {3}  {1,3,5} {5}
// {}       {1,3}      {1,3}    {3,5}

// 2         4                       6
// {}
// {2}   {2} {2,4} {4}    {2} {2,4} {4}  {2,4,6} {4,6} {6}

//  1        2                3                                   4             5 6 7
// {1}  {1} {1,2}   {1}{1,2} {2,3} {3}    {1} {1,2} {2,3} {3}  {2,3,4} {3,4}
// {}       {2}     {2}      {1,2,3}      {2} {1,2,3}          {1,2,3,4} {4}

class Solution {
  bool isOdd(x) => x % 2 == 1;
  int numOfSubarrays(List<int> arr) {
    int a = 0, b = 0, res = 0, M = 1000000007;
    for (final x in arr) {
        res = (res + a) % M;
        if (isOdd(x)) (a, b) = (b+1, a);
        else          ++b;
    }
    return (res + a) % M;
  }
}
